#include "ft.h"

int	main(int argc, char **argv)
{
	int i;

	i = 0;
	if (argc == 1 || (argv[1][0] == '-'))
		ft_n_argc();
	else
		while (++i < argc)
		{
			ft_cat(argv[i]);
		}
	return (0);
}
