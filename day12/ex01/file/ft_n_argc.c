#include <unistd.h>
#include "ft.h"

void	ft_n_argc(void)
{
	int		ret;
	char	buf[2];

	ret = (read(0, buf, 1));
	while (ret)
	{
		buf[ret] = '\0';
		ft_putstr(buf);
		ret = (read(0, buf, 1));
	}
}
